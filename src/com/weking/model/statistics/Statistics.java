package com.weking.model.statistics;

public class Statistics {
    private Integer id;

    private Integer userId;

    private Integer fansTotal;

    private Integer winTotal;

    private String nickname;

    private String account;

    private String picheadUrl;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Integer getFansTotal() {
        return fansTotal;
    }

    public void setFansTotal(Integer fansTotal) {
        this.fansTotal = fansTotal;
    }

    public Integer getWinTotal() {
        return winTotal;
    }

    public void setWinTotal(Integer winTotal) {
        this.winTotal = winTotal;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public String getAccount() {
        return account;
    }

    public void setAccount(String account) {
        this.account = account;
    }

    public String getPicheadUrl() {
        return picheadUrl;
    }

    public void setPicheadUrl(String picheadUrl) {
        this.picheadUrl = picheadUrl;
    }
}