package com.weking.model.live;

public class UserChat {
    private Integer id;

    private Integer chatPrice;

    private Boolean isDisturb;

    private Integer chatTime;

    private Long addTime;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getChatPrice() {
        return chatPrice;
    }

    public void setChatPrice(Integer chatPrice) {
        this.chatPrice = chatPrice;
    }

    public Boolean getIsDisturb() {
        return isDisturb;
    }

    public void setIsDisturb(Boolean isDisturb) {
        this.isDisturb = isDisturb;
    }

    public Integer getChatTime() {
        return chatTime;
    }

    public void setChatTime(Integer chatTime) {
        this.chatTime = chatTime;
    }

    public Long getAddTime() {
        return addTime;
    }

    public void setAddTime(Long addTime) {
        this.addTime = addTime;
    }
}