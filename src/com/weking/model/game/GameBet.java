package com.weking.model.game;

public class GameBet {
    private Long id;

    private Integer userId;

    private Long gameTime;

    private Long gameId;

    private Integer betCoin;

    private Integer incomeCoin;

    private Integer anchorId;

    private Byte gameType;

    public Byte getGameType() {
        return gameType;
    }

    public void setGameType(Byte gameType) {
        this.gameType = gameType;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Long getGameTime() {
        return gameTime;
    }

    public void setGameTime(Long gameTime) {
        this.gameTime = gameTime;
    }

    public Long getGameId() {
        return gameId;
    }

    public void setGameId(Long gameId) {
        this.gameId = gameId;
    }

    public Integer getBetCoin() {
        return betCoin;
    }

    public void setBetCoin(Integer betCoin) {
        this.betCoin = betCoin;
    }

    public Integer getIncomeCoin() {
        return incomeCoin;
    }

    public void setIncomeCoin(Integer incomeCoin) {
        this.incomeCoin = incomeCoin;
    }

    public Integer getAnchorId() {
        return anchorId;
    }

    public void setAnchorId(Integer anchorId) {
        this.anchorId = anchorId;
    }
}